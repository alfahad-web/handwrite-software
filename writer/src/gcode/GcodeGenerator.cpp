#include "GcodeGenerator.h"

#include <QLineF>
#include <QStringList>
#include <QtMath>

namespace {
QString fmtMm(double v) {
    return QString::number(v, 'f', 3);
}

QString xyLine(const char *cmd, double xMm, double yMm) {
    return QStringLiteral("%1 X%2 Y%3").arg(QLatin1String(cmd), fmtMm(xMm), fmtMm(yMm));
}

double segmentLengthCm(const PathSegment &seg) {
    const QVector<QPointF> &pts = seg.pointsCm;
    if (pts.size() < 2) return 0;
    if (seg.travel) return QLineF(pts[0], pts[1]).length();
    double len = 0;
    for (int i = 1; i < pts.size(); ++i)
        len += QLineF(pts[i - 1], pts[i]).length();
    return len;
}
}

QString GcodeGenerator::generate(const PathBuildResult &path, const AppSettings *settings) {
    if (!settings) return QStringLiteral("; No settings\n");

    const double penUpZ = settings->penUpZ();
    const double penDownZ = settings->penDownZ();
    const int feed = qMax(1, int(qRound(settings->feedRateMmPerMin())));

    QStringList lines;
    lines << QStringLiteral("; GRBL 1.1h — generated by WriterQt");
    lines << QStringLiteral("; Units: mm, absolute positioning");
    lines << QStringLiteral("G21");
    lines << QStringLiteral("G90");
    lines << QStringLiteral("G0 X0 Y0");
    lines << QStringLiteral("G0 Z%1").arg(fmtMm(penUpZ));
    lines << QStringLiteral("F%1").arg(feed);

    bool hasMotion = false;
    for (const PathSegment &seg : path.segments) {
        if (segmentLengthCm(seg) <= 1e-9) continue;
        const QVector<QPointF> &pts = seg.pointsCm;
        if (pts.size() < 2) continue;

        if (seg.travel) {
            const double x0 = pts[0].x() * 10.0;
            const double y0 = pts[0].y() * 10.0;
            const double x1 = pts[1].x() * 10.0;
            const double y1 = pts[1].y() * 10.0;
            lines << QStringLiteral("G0 Z%1").arg(fmtMm(penUpZ));
            lines << xyLine("G0", x0, y0);
            lines << xyLine("G0", x1, y1);
            hasMotion = true;
        } else {
            const double x0 = pts[0].x() * 10.0;
            const double y0 = pts[0].y() * 10.0;
            lines << QStringLiteral("G0 Z%1").arg(fmtMm(penUpZ));
            lines << xyLine("G0", x0, y0);
            lines << QStringLiteral("G0 Z%1").arg(fmtMm(penDownZ));
            for (int i = 1; i < pts.size(); ++i) {
                const double x = pts[i].x() * 10.0;
                const double y = pts[i].y() * 10.0;
                lines << xyLine("G1", x, y);
            }
            hasMotion = true;
        }
    }

    if (!hasMotion) {
        return QStringLiteral("; No drawable strokes\n");
    }

    lines << QStringLiteral("G0 Z%1").arg(fmtMm(penUpZ));
    lines << QStringLiteral("G0 X0 Y0");
    return lines.join(QLatin1Char('\n')) + QLatin1Char('\n');
}
